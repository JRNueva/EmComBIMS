export function SORTED_OFFICIALS () {

    const getPositionRank = (position) => POSITION_HIERARCHY.indexOf(position);
    const getTermRank = (term) => TERM_RANKING[term] || 0;

    return OFFICIALS_GALLERY.slice().sort((a, b) => {
        
        const rankA = getPositionRank(a.position);
        const rankB = getPositionRank(b.position);
        
        if (rankA !== rankB) {
            return rankA - rankB;
        }
        
        if (a.position === 'Kagawad' && b.position === 'Kagawad') {
            const termRankA = getTermRank(a.term);
            const termRankB = getTermRank(b.term);
            return termRankB - termRankA;
        }
        
        return 0;
    });
};

